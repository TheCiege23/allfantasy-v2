/**
 * Builds supabase_alter_all.sql with strict ordering:
 * 1) CREATE TYPE IF NOT EXISTS (PostgreSQL 15+)
 * 2) CREATE TABLE IF NOT EXISTS (no FK section)
 * 3) ALTER TABLE ADD COLUMN IF NOT EXISTS (grouped by table)
 * 4) CREATE INDEX IF NOT EXISTS
 *
 * Drops all -- AddForeignKey statements.
 */
import { execSync } from "child_process";
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const outFile = path.join(root, "supabase_alter_all.sql");

function fetchRawDiff() {
  return execSync(
    "npx prisma migrate diff --from-empty --to-schema-datamodel prisma/schema.prisma --script",
    { cwd: root, encoding: "utf8", maxBuffer: 64 * 1024 * 1024 },
  );
}

function splitChunks(raw) {
  const lines = raw.replace(/\r\n/g, "\n").split("\n");
  /** @type {{ kind: string; lines: string[] }[]} */
  const chunks = [];
  let cur = null;
  for (const line of lines) {
    const m = line.match(/^-- (CreateEnum|CreateTable|CreateIndex|AddForeignKey)$/);
    if (m) {
      if (cur) chunks.push(cur);
      cur = { kind: m[1], lines: [line] };
    } else if (cur) {
      cur.lines.push(line);
    } else {
      if (!chunks.length && line.trim()) {
        chunks.push({ kind: "preamble", lines: [line] });
      } else if (chunks.length && chunks[0].kind === "preamble") {
        chunks[0].lines.push(line);
      }
    }
  }
  if (cur) chunks.push(cur);
  return chunks;
}

function ensureCreateTypeIfNotExists(sql) {
  return sql.replace(/^(\s*)CREATE TYPE "/gm, "$1CREATE TYPE IF NOT EXISTS \"");
}

function ensureCreateTableIfNotExists(sql) {
  return sql.replace(/CREATE TABLE "/g, 'CREATE TABLE IF NOT EXISTS "');
}

function ensureIndexIfNotExists(sql) {
  let s = sql;
  s = s.replace(/CREATE UNIQUE INDEX "/g, 'CREATE UNIQUE INDEX IF NOT EXISTS "');
  s = s.replace(/CREATE INDEX "/g, 'CREATE INDEX IF NOT EXISTS "');
  return s;
}

function stripReferencesFromCreateTable(sql) {
  return sql.replace(/\s+REFERENCES\s+"[^"]+"\s*\([^)]+\)(\s+ON\s+DELETE\s+\w+)?(\s+ON\s+UPDATE\s+\w+)?/gi, "");
}

/**
 * @param {string} createTableChunk full chunk including -- CreateTable line
 */
function parseTableNameAndColumns(createTableChunk) {
  const body = createTableChunk.replace(/^-- CreateTable\n/m, "");
  const open = body.indexOf("(");
  const close = body.lastIndexOf(");");
  if (open === -1 || close === -1) return null;
  const header = body.slice(0, open);
  const m = header.match(/CREATE TABLE (?:IF NOT EXISTS\s+)?"([^"]+)"/);
  if (!m) return null;
  const tableName = m[1];
  const inner = body.slice(open + 1, close);
  const colLines = inner.split("\n");
  /** @type {{ name: string; def: string }[]} */
  const cols = [];
  for (let line of colLines) {
    line = line.trim();
    if (!line) continue;
    if (line.startsWith("CONSTRAINT")) continue;
    const col = line.match(/^"([^"]+)"\s+(.+)$/);
    if (!col) continue;
    let rest = col[2].replace(/,\s*$/, "").trim();
    if (!rest) continue;
    cols.push({ name: col[1], def: rest });
  }
  return { tableName, cols };
}

function buildSection3(tableChunks) {
  const parts = [];
  for (const ch of tableChunks) {
    const text = ch.lines.join("\n");
    const parsed = parseTableNameAndColumns(text);
    if (!parsed || !parsed.cols.length) continue;
    parts.push("");
    parts.push(`-- Table: "${parsed.tableName}"`);
    for (const { name, def } of parsed.cols) {
      parts.push(`ALTER TABLE "${parsed.tableName}" ADD COLUMN IF NOT EXISTS "${name}" ${def};`);
    }
  }
  return parts.join("\n").trim() + "\n";
}

function main() {
  const raw = fetchRawDiff();
  const chunks = splitChunks(raw);

  const enums = chunks.filter((c) => c.kind === "CreateEnum");
  const tables = chunks.filter((c) => c.kind === "CreateTable");
  const indexes = chunks.filter((c) => c.kind === "CreateIndex");
  const fks = chunks.filter((c) => c.kind === "AddForeignKey");
  void fks;

  const s1 = enums.map((c) => ensureCreateTypeIfNotExists(c.lines.join("\n"))).join("\n\n");

  let s2 = tables.map((c) => ensureCreateTableIfNotExists(stripReferencesFromCreateTable(c.lines.join("\n")))).join("\n\n");

  const s3 = buildSection3(tables);

  let s4 = indexes.map((c) => ensureIndexIfNotExists(c.lines.join("\n"))).join("\n\n");

  const out = `-- =============================================================================
-- supabase_alter_all.sql — generated by scripts/build-supabase-alter-all.mjs
-- Re-run: node scripts/build-supabase-alter-all.mjs
-- Foreign keys omitted (manage in Supabase). ${fks.length} FK statements dropped.
-- =============================================================================

-- SECTION 1 — CREATE TYPE IF NOT EXISTS
${s1.trim()}

-- SECTION 2 — CREATE TABLE IF NOT EXISTS
${s2.trim()}

-- SECTION 3 — ALTER TABLE ADD COLUMN IF NOT EXISTS (legacy-safe; grouped by table)
${s3.trim()}

-- SECTION 4 — CREATE INDEX IF NOT EXISTS
${s4.trim()}
`;

  fs.writeFileSync(outFile, out.replace(/\n{3,}/g, "\n\n"), "utf8");
  console.log(`Wrote ${outFile}`);
  console.log(`Enums: ${enums.length}, Tables: ${tables.length}, Indexes: ${indexes.length}, FKs dropped: ${fks.length}`);
}

main();
